public class Moeda {
    int id;
    int x;
    int y;
    
    public Moeda(int id){
        Random rand = new Random();
        x = rand.nextInt(550)+220;
        y = rand.nextInt(550)+20;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
